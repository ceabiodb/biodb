#!/bin/bash
set -e

echo
echo '****************************************************************'
echo OFFLINE TESTS
echo '****************************************************************'
make test

echo
echo '****************************************************************'
echo CHECK
echo '****************************************************************'
make check

echo
echo '****************************************************************'
echo INSTALL
echo '****************************************************************'
sudo make install

echo
echo '****************************************************************'
echo EXAMPLES
echo '****************************************************************'
make ex 

git branch

if [ -n "$(git branch | egrep '^\* (release|master)')" ] ; then
	echo
	echo '****************************************************************'
	echo ONLINE TESTS
	echo '****************************************************************'
	MODES=online make test
fi
